    public void paintComponent(Graphics g){
        super.paintComponent(g); 
        int n = 4;//recursion time
        int x = 300, y = 300;// starting point of the tree
        int length = 300;// side length of the tree
        drawHTree(g, n, x, y, length);
    }
    
    public static void drawHTree(Graphics g, int n, int x, int y, int length) {
        if (n > 0){
            //calculate the four ends of the tree
            //which are the starting points of new trees
            int x1 = x - length/2;
            int y1 = y - length/2;
            int x2 = x + length/2;
            int y2 = y + length/2;      
            //draw H
            g.drawLine(x1, y1, x1, y2);//left "|"
            g.drawLine(x2, y1, x2, y2);//right "|"
            g.drawLine(x1,  y, x2,  y);//middle "â€”"
            //draw 4 new H-trees whose side length is half of the original tree
            drawHTree(g, n-1, x1, y1, length/2);
            drawHTree(g, n-1, x1, y2, length/2);
            drawHTree(g, n-1, x2, y1, length/2); 
            drawHTree(g, n-1, x2, y2, length/2);
        }
    }

